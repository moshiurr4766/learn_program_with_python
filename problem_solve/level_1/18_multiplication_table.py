"""Problem: Print multiplication table."""

# Problem:
# Print the multiplication table of a number.

number = 7

# Solution:
for i in range(1, 11):
    print(f"{number} x {i} = {number * i}")

# Logic:
# 1. Loop from 1 to 10.
# 2. Multiply the number by each loop value.
# 3. Print the formatted result.

# Explanation:
# Tables are a good practice for loops and formatting.
