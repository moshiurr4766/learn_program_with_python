"""Problem: Check whether a number is even or odd."""

# Problem:
# Read a number and tell if it is even or odd.

number = 17

# Solution:
if number % 2 == 0:
    print(number, "is even")
else:
    print(number, "is odd")

# Logic:
# 1. Use the modulo operator.
# 2. If the remainder is 0, the number is even.
# 3. Otherwise, it is odd.

# Explanation:
# Modulo gives the remainder after division.
