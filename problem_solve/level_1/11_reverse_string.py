"""Problem: Reverse a string."""

# Problem:
# Reverse the characters of a string.

text = "python"

# Solution:
reversed_text = text[::-1]
print("Reversed string:", reversed_text)

# Logic:
# 1. Use slice notation.
# 2. Step -1 moves from right to left.
# 3. The result becomes the reverse of the string.

# Explanation:
# Python slicing makes string reversal very short.
