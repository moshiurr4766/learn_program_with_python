"""Problem: Sum all elements in a list."""

# Problem:
# Add every number in a list.

numbers = [10, 20, 30, 40]

# Solution:
total = 0

for number in numbers:
    total += number

print("Total sum:", total)

# Logic:
# 1. Start with zero.
# 2. Go through each list item.
# 3. Add each item to the total.

# Explanation:
# This is the basic pattern behind many list problems.
