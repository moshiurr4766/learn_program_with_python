"""Array basics with multiple examples."""

# Example 1: traverse a list
numbers = [10, 20, 30, 40]
print("Traverse:")
for number in numbers:
    print(number)

# Example 2: append and update
scores = [50, 60, 70]
scores.append(80)
scores[1] = 65
print("Updated list:", scores)

# Example 3: sum and max
print("Sum:", sum(numbers))
print("Max:", max(numbers))

# Logic:
# 1. Lists store ordered values.
# 2. Indexing lets us update specific items.
# 3. Built-in functions help with common operations.

