"""Arithmetic, comparison, and logical operators."""

a = 10
b = 3

print("Arithmetic:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)

print("\nComparison:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a <= b:", a <= b)

print("\nLogical:")
has_id = True
is_adult = False
print("has_id and is_adult:", has_id and is_adult)
print("has_id or is_adult:", has_id or is_adult)
print("not has_id:", not has_id)

